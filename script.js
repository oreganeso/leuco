let currentSection = ""; // Sección actual seleccionada
let currentIndex = 0; // Índice de la imagen actual

function showSection(section) {
    // Cambia el título de la galería y muestra las imágenes correspondientes
    document.getElementById("gallery-tittle").textContent = section;
    currentSection = section;
    currentIndex = 0;
    showImage();

    // Muestra las flechas cuando hay más de una imagen en la sección
    if (images[section].length > 1) {
        document.getElementById("prev-arrow").style.display = 'block';
        document.getElementById("next-arrow").style.display = 'block';
    } else {
        // Oculta las flechas si solo hay una imagen
        document.getElementById("prev-arrow").style.display = 'none';
        document.getElementById("next-arrow").style.display = 'none';
    }
}

function showImage() {
    const galleryImg = document.getElementById("gallery-img");
    galleryImg.innerHTML = ""; // Limpiar contenido previo

    // Obtener la imagen actual de la sección
    const sectionImages = images[currentSection];
    if (sectionImages && sectionImages.length > 0) {
        const imgElement = document.createElement("img");
        imgElement.src = sectionImages[currentIndex];
        imgElement.alt = currentSection;
        imgElement.style.maxWidth = "80%"; // Ajuste para mantener la imagen más pequeña
        imgElement.style.borderRadius = "10px"; // Opcional: bordes redondeados
        galleryImg.appendChild(imgElement);
    }
}

// Función para avanzar a la siguiente imagen
function nextImage() {
    const sectionImages = images[currentSection];
    if (sectionImages) {
        currentIndex = (currentIndex + 1) % sectionImages.length;
        showImage();
    }
}

// Función para retroceder a la imagen anterior
function prevImage() {
    const sectionImages = images[currentSection];
    if (sectionImages) {
        currentIndex = (currentIndex - 1 + sectionImages.length) % sectionImages.length;
        showImage();
    }
}
